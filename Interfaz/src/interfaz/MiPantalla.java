    void suma() {

        int num1 = 0;
        int num2 = 0;

        try {
            String valor1 = txtNum1.getText();
            String valor2 = txtNum2.getText();

            if (valor1.equals("") ) {
                return;
            }

            num1 = Integer.parseInt(valor1); // retorna un String
           
            if( valor2.equals("")){
                return;
            }
            
            num2 = Integer.parseInt(valor2); // retorna un String

            int resultado = num1 + num2;

            lblResultado.setText(String.valueOf(resultado));

        } catch (NumberFormatException e) {

            System.out.println(e.getMessage());

            JOptionPane.showMessageDialog(this, "Los valores enviados no son v√°lidos", "Error!", JOptionPane.ERROR_MESSAGE);
        }

    }

